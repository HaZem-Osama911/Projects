                <p>تزيد بعض الاضطرابات الطبية، بما في ذلك متلازمة إكس الهش، والتصلب الدرني، ومتلازمة توريت، والصرع،
                    من خطر الإصابة بالتوحد.</p>
                <h3>عمر الوالدين:</h3>
                <p>يرتبط تقدم عمر الأب بارتفاع احتمالية الإصابة بمرض التوحد.</p>
                <h3>المضاعفات:</h3>
                <ul class="dot">
