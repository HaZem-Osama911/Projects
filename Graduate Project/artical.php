                        وأن هناك من يكافح مثله للتعبير عن نفسه.</p>
                    <p>الكلمات هي أداتنا للتعبير عن أنفسنا ونتمكن من خلالها البوح عن مكنونات صدورنا، وهي -أيضًا- وسيلتنا
