                <h3 class="title">بحة الصوت</h3>
                <p>قد يظهر في صوت طفلك بحة غريبة ومفاجئة، فتشعرين معها بالقلق. والحقيقة أن العديد من الأمهات يقلقن إلى
                    الحد الذي تعتقد بعضهن أنها قد تكون علامة على مرض خطير، في هذا المقال نخبرك بأسباب إصابة طفلكِ ببحة
                    الصوت، وكيف يمكنكِ مساعدته على التخلص منها.</p>
                <h3>بحة الصوت عند الطفل</h3>
                <p>بحة الصوت حالة شائعة جدًا عند الأطفال. وتسمى أيضًا خلل النطق، وهو المصطلح المستخدم لوصف التغيير في
                    جودة الصوت، يمكن أن تكون جودة الصوت متوترة أو خشنة أو مرتجفة أو ضعيفة، وقد تلاحظين رنينًا غير طبيعي.
                </p>
                <p>عادًة ما تكون بحة الصوت عند الطفل متعلقة بالطريقة التي يستخدم بها صوته خلال اليوم، وليس بمرض خطير.
                    إذا كان طفلك لديه هذه البحة، فقد تكون هناك حاجة إلى تقييم مشترك من اختصاصي الأنف والأذن والحنجرة
                    واختصاصي أمراض النطق في عيادة صوتية، فبحة الصوت تنتج عن مجموعة متنوعة من الأسباب ومن المهم العثور
                    على سببها للتمكن من علاجها.</p>
                <p>كما يمكنكِ مساعدة طفلك بمنعه من الصراخ وإراحة صوته حتى موعد زيارة الطبيب.</p>
                <h3>أسباب بحة الصوت عند الأطفال</h3>
