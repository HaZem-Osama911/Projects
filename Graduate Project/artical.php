                    مع حركات مدربه.</p>
                <p>من المهم توفير الجو النفسى الملائم للمريض ومن غير المقبول السخرية من الطفل أو الضحك على طريقة نطقه
                    لأن لهذا أثر سلبي على نفسيته وشخصيته.</p>
