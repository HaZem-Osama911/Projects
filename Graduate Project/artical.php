                <h3>علاج بحة الصوت عند الأطفال</h3>
                <p>محتويات بحة الصوت عند الطفل أسباب بحة الصوت عند الأطفال علاج بحة الصوت عند الأطفال قد يظهر في صوت
                    طفلك بحة غريبة ومفاجئة، فتشعرين معها بالقلق. والحقيقة أن العديد من الأمهات يقلقن إلى الحد الذي تعتقد
                    بعضهن أنها قد تكون علامة على مرض خطير، في هذا المقال نخبرك بأسباب إصابة طفلكِ ببحة الصوت، وكيف
                    يمكنكِ مساعدته على التخلص منها. بحة الصوت عند الطفل بحة الصوت حالة شائعة جدًا عند الأطفال. وتسمى
                    أيضًا خلل النطق، وهو المصطلح المستخدم لوصف التغيير في جودة الصوت، يمكن أن تكون جودة الصوت متوترة أو
                    خشنة أو مرتجفة أو ضعيفة، وقد تلاحظين رنينًا غير طبيعي. عادًة ما تكون بحة الصوت عند الطفل متعلقة
                    بالطريقة التي يستخدم بها صوته خلال اليوم، وليس بمرض خطير. إذا كان طفلك لديه هذه البحة، فقد تكون هناك
                    حاجة إلى تقييم مشترك من اختصاصي الأنف والأذن والحنجرة واختصاصي أمراض النطق في عيادة صوتية، فبحة
                    الصوت تنتج عن مجموعة متنوعة من الأسباب ومن المهم العثور على سببها للتمكن من علاجها. كما يمكنكِ
                    مساعدة طفلك بمنعه من الصراخ وإراحة صوته حتى موعد زيارة الطبيب. اقرئي أيضًا: كيف تهدئين صراخ طفلك
                    أسباب بحة الصوت عند الأطفال هناك عديد من أسباب بحة الصوت، أهمها: الاستخدام المفرط للصوت، مثل الصراخ
                    أو التحدث بصوت مرتفع أو البكاء فترة طويلة. التهاب الحنجرة ما يسبب تورمًا في الحبال الصوتية نتيجة
                    الإفراط في استخدام الصوت أو بسبب مرض فيروسي الحساسية فتسبب تورمًا في الحبال الصوتية من السعال وقطرات
                    الأنف والعطس. الارتجاع الحنجري البلعومي، وهو ارتجاع حمض المعدة يسبب تورم وتهيج في الأنسجة الرقيقة
                    للحبال الصوتية والحلق. عقيدات الحبل الصوتي، وهي مسامير صغيرة في الحبال الصوتية تحدث بسبب الإفراط في
                    استخدام الصوت أو إصابة الحبل الصوتي التي تحدث مع الصراخ. الأورام الحميدة في الحبل الصوتي، وهو نمو
                    صغير في الحبل الصوتي يشبه البثرة الناتجة عن الإفراط في استخدام الصوت أو إصابة الحبل الصوتي خلال
                    الصراخ. كيس الحبل الصوتي، نمو صغير في الحبل الصوتي يملأ عادة بالمخاط ويسبب صوتًا خشنًا. الورم
                    الحليمي للحبل الصوتي بسبب عدوى فيروسية في الطيات الصوتية التي يسببها فيروس الورم الحليمي البشري.
                    تندّب في الحبال الصوتية من صدمة أو إصابة في الحبال الصوتية أو من أنابيب التنفس إذا تعرض الطفل لجراحة
                    سابقة، وتكون في مكان الجراحة أو خلال التنفس الصناعي. شلل الحبل الصوتي، قد يولد الطفل بتشوّه في
                    الحنجرة أو مشكلة عصبية تسبب بحة في الصوت. يمكن أن تشمل أسباب بحة الصوت هذه شلل الحبل الصوتي، وتشنج
                    الحنجرة، والخراجات، والشقوق في صندوق الصوت. تتضمن بعض المتلازمات الجينية الموروثة أيضًا تشوهات تسبب
                    بحة في الصوت. تلف الرقبة أو الحبال الصوتية من خلال الصدمة. تتضمن بعض الأمثلة الابتلاع الكاوي
                    (الأحماض، السموم)، وضع أنبوب التنفس، وضع أنبوب التغذية، صدمة الولادة، أو صدمات أخرى (حوادث السيارات
                    أو الدراجات). ضرر العصب الحنجري الراجع الذي يحرك الحبال الصوتية قد يؤدي إلى بحة في الصوت. في بعض
                    الأحيان يولد الطفل مع تلف هذا العصب. قد تؤدي الجراحة في الصدر وحول القلب والأوعية الدموية الكبيرة
                    أيضًا إلى تلف الأعصاب. إذا كان لدى طفلكِ صوت أجش لأكثر من أربعة أسابيع، فيجب أن يقيمه طبيب الأنف
                    والأذن والحنجرة، إذ يمكن أن تتطور البحة أو تكون علامة على حالة أكثر خطورة. علاج بحة الصوت عند
                    الأطفال يمكن معالجة بحة الصوت بطرق متنوعة. اعتمادًا على التشخيص، قد يوصي طبيب الأنف والأذن والحنجرة
                    بما يلي للعلاج في المنزل:</p>
                <p>- راحة الصوت بضعة أيام، وتجنب الحديث والصراخ.</p>
                <p>- شرب كثير من السوائل المرطبة. قد تخفف السوائل بحة الصوت وترطب حلقه.</p>
                <p>- أخذ حمام ساخن، إذ يساعد البخار الناتج عن الاستحمام على فتح مجري الهواء وتوفير الرطوبة.</p>
                <p>- ترطيب الحلق عن طريق امتصاص المستحلبات أو مضغ العلكة. هذا يحفز إفراز اللعاب وقد يساعد على تهدئة
                    حلقه.</p>
                <p> في حالات نادرة وعند الضرورة، تجرى جراحة من أجل إزالة الزوائد في الحنجرة، أو إعادة وضع الحبال الصوتية
                    المشلولة الناجمة عن الشلل الصوتي.</p>
                <h3 class="title">متلازمات الوهن العضل</h3>
                <p>متلازمات الوهن العضلي الخلقي هي مجموعة من الحالات الوراثية النادرة الناجمة عن تغير في جين يسبب ضعفًا
                    في العضلات يتفاقم مع الأنشطة البدنية. قد تتأثر أي عضلات تُستخدم للحركة، بما في ذلك العضلات التي
                    تتحكم في الكلام والمضغ والبلع والرؤية والرمش والتنفس والمشي.</p>
                <br>
