                    <h3 class="title">اضطراب الحركات النمطيّة</h3>
                    <p>اضطراب الحركات النمطيّة (بالإنجليزيّة: Stereotypic Movement Disorder)، ويُسمّى أيضًا اضطراب
                        الحركة
                        التكراريّ، هو اضطراب حركيّ ينشأ في مرحلة الطفولة، عادةً قبل مرحلة المدرسة الابتدائيّة، ويتضمّن
                        حركات
                        متكرّرة دون هدف. ومن الأمثلة على الحركات النمطيّة: تقليب الكفّين، وتأرجح الجسد، وخبط الرأس، وعضّ
                        النفس. لا يُشخَّص الطفل باضطراب الحركات النمطيّة إلّا إذا كانت الحركات المتكرّرة تسبّب له ضيقًا،
                        وتؤدّي إلى خلل في الأداء الوظيفيّ اليوميّ، أو تَنتج عنها أذيّة النفس.</p>
                    <br>
                    <p>الحركات النمطيّة البسيطة (مثل: التأرجح للخلف والأمام) شائعة لدى الأطفال الصغار، ولا تشير إلى
                        اضطراب
                        حركيّ. أمّا الحركات النمطيّة المعقّدة فهي أقلّ شيوعًا، وتحدث لدى (3%) إلى (4%) من الأطفال. عادةً
                        ما
                        تُشخَّص الحركات النمطيّة لدى الأشخاص ذوي الإعاقات الذهنيّة وحالات النماء العصبيّ؛ لكنّها توجد
                        أيضًا
                        لدى الأطفال ذوي النموّ النموذجيّ. في حين أنّ الأفعال قد تكون أو تظهر دون هدف، من الممكن أن تقلّل
                        الحركات النمطيّة من القلق كاستجابة لعوامل مجهدة خارجيّة.</p>
                    <h3> أعراض اضطراب الحركات النمطيّة</h3>
                </section>
                <p>عادةً ما تتمّ ملاحظة الحركات النمطيّة خلال أوّل (3) سنوات من العمر. تتضمّن أعراض اضطراب الحركات
                    النمطيّة ما يلي:</p>
                <p>
                    -حركات تكراريّة، ومتعمّدة، ومن دون هدف. ومن والحركات النمطيّة: هزّ اليد، أو عصرها، أو التلويح بها،
                    وخبط الرأس، وضرب النفس، وعضّها.

                    <br>
                    -تتعارض الحركات مع الحياة اليوميّة، مثل عرقلة الدراسة والعلاقات.
                    <br>
                    -يبدأ السلوك في مرحلة الطفولة.
                    <br>
                    -لا يعود السلوك إلى اضطراب آخر، مثل اضطراب الوسواس القهريّ، أو السلوكيّات المتكرّرة التي تركّز على
                    الجسم.
                    <br> <br>
                    تختلف أنواع الحركات التكراريّة بشكل واسع، ويظهر كلّ طفل بسلوك فرديّ نمطيّ «مُوقَّع». من المحتمل أن
                    تتزايد الحركات التكراريّة مع الملل، والضغط، والحماس، والإرهاق. قد يتوقّف بعض الأطفال عن أداء الحركات
                    التكراريّة إذا كانوا محطّ انتباه أو مشتّتي الذهن؛ في حين أنّ البعض الآخر قد لا يتمكّنون من إيقاف
                    حركاتهم التكراريّة. يمكن تصنيف اضطراب الحركات النمطيّة على أنّه خفيف، أو متوسّط، أو شديد. تسمح
                    الحالة الخفيفة للشخص بكبت حركات الجسم بسهولة؛ بينما تتطلّب الحالة المتوسّطة تدابير وقائيّة أو تعديل
                    السلوك. أمّا الحالة الشديدة فتتطلّب المراقبة المستمرّة والتدابير الوقائيّة لتجنّب الإصابة الخطيرة.
                </p>
                <h3>ما هي الحركات النمطيّة؟</h3>
                <p>الحركات النمطيّة هي حركات لا إراديّة، وإيقاعيّة، ومتكرّرة، ويمكن التنبّؤ بها. تبدو الحركات النمطيّة
                    هادفة؛ لكنّها لا تخدم وظيفة أو غرضًا واضحًا. ومن الأمثلة على الحركات النمطيّة: التلويح باليد،
                    والتدوير التكراريّ بها، وتقليب الذراعين، وتَلَوّي الأصابع، وغيرها من الأمثلة المذكورة أعلاه.</p>
                <h3>كيف يُشخَّص اضطراب الحركات النمطيّة؟</h3>
                <p>الطريقة الأوّليّة لمعرفة ما إذا كان لدى الشخص اضطراب الحركات النمطيّة هو وجود الحركات النمطيّة التي
                    تم ذكرها في ما سبق. يكون الاضطراب أكثر وضوحًا، خاصّةً عند الأطفال الصغار، في حال وجود حركات الجسد
                    التي قد تؤدّي إلى جرح ذاتيّ، مثل: عضّ الشفتين، أو وخز العينين. إذا كان لدى الطفل حركات نمطيّة من دون
                    علامات أخرى لإعاقة نمائيّة، يجب عليه رؤية طبيب أطفال.</p>
                <h3>هل الحركات النمطيّة هي نفسها سلوكيّات التنبيه الذاتيّ؟</h3>
                <p>بشكل عامّ، الإجابة هي: نعم؛ وذلك على الرغم من استخدام مصطلح «سلوك التنبيه الذاتيّ» (بالإنجليزيّة:
                    Self-Stimulatory Behavior، واختصاره: Stimming) عامّةً في سياق الحديث عن الأشخاص ذوي اضطراب طيف
                    التوحّد. بالنسبة للشخص ذي اضطراب طيف التوحّد، يشير سلوك التنبيه الذاتيّ إلى نفس السلوكيات النمطيّة
                    التي تمّ ذكرها فيما سبق، بما فيها تقليب الكفّين، أو نقرة الأصابع، أو الدوران.</p>
                <h3>ما هي أسباب اضطراب الحركات النمطيّة؟</h3>
                <p>لا يزال السبب المباشر لاضطراب الحركات النمطيّة غير معروف؛ لكن هناك العديد من عوامل الخطر المرتبطة
                    بظهوره. على سبيل المثال، من الممكن أن تؤدّي العزلة الاجتماعيّة إلى التنبيه الذاتيّ على شكل حركات
                    نمطيّة. كما يمكن للضغط المحيط (مثل وجود صعوبة في المدرسة أو المنزل) استثارة السلوك النمطيّ. قد يكون
                    هناك أيضًا عنصر جينيّ للحالة.
                    <br>
                    إضافةً إلى ما سبق، قد يغيّر الخوف من حالة الشخص الوظيفيّة؛ ممّا قد يؤدّي إلى زيادة تواتر السلوكيّات
                    النمطيّة. يرتبط أداء الوظائف المعرفيّة المنخفض بخطر أكبر للسلوكيّات النمطيّة وأقلّ استجابة ملائمة
                    للتدخّلات. أظهرت الأبحاث الأخيرة احتماليّة وجود اختلال وظيفيّ في مسارات حمض الجاما أمينوبوتيريك
                    (بالإنجليزيّة: Gamma-Aminobutyric Acid) في الدماغ.
                    <br>
                    يكون خطر الإصابة باضطراب الحركات النمطيّة أكبر عند الأشخاص ذوي الإعاقات الذهنيّة الشديدة. في الواقع،
                    يمكن عادةً قمع الحركات النمطيّة أو تقليلها بمرور الوقت لدى الأطفال ذوي النمو النموذجيّ. أمّا
                    السلوكيّات النمطيّة المؤذية للذات لدى الأطفال ذوي الإعاقات الذهنيّة فيمكن أن تستمرّ لعدّة سنوات.

                </p>
                <h3>هل يرتبط اضطراب الحركات النمطيّة باضطراب طيف التوحّد؟</h3>
                <p>يمكن أن تكون الحركات التكراريّة عرضًا ظاهرًا من أعراض اضطراب طيف التوحّد. بما أنّ الحركات النمطيّة
                    سِمة من سِمات اضطراب طيف التوحّد، لا يتمّ تشخيص اضطراب الحركات النمطيّة عندما تكون السلوكيّات
                    التكراريّة مفسّرة بشكل أفضل بوجود اضطراب طيف التوحّد. ومع ذلك، عندما تتسبّب الحركات النمطيّة في
                    إيذاء النفس وتصبح محطّ تركيز للعلاج، يصبح كِلا التشخيصين -اضطراب الحركات النمطيّة واضطراب طيف
                    التوحّد- ملائمين.</p>
                <h3>هل يتأثّر اضطراب الحركات النمطيّة بالجينات؟</h3>
                <p>في إحدى الدراسات، أفاد (25%) من الأطفال الذين لديهم حركات نمطيّة وجود تاريخ عائليّ للحركات النمطيّة؛
                    ممّا يقترح أنّ الجينات تؤدّي دورًا في هذا الاضطراب.</p>
                <h3>ما هو علاج اضطراب الحركات النمطيّة؟</h3>
                <p>يمكن أن يشمل علاج اضطراب الحركات النمطيّة العلاج النفسيّ والاستراتيجيّات السلوكيّة؛ بهدف التقليل من
                    الحركات التكراريّة، والحدّ من خطر إيذاء النفس. عادةً ما تُستخدَم تقنيّة علاجيّة تُدعى التعزيز
                    التفريقيّ (بالإنجليزيّة: Differential Reinforcement)، والتي تعتمد على التعزيز الإيجابيّ لتعليم الطفل
                    كيفيّة تعديل سلوكه. حتّى الوقت الحاليّ لم تثبت العقاقير فعّاليّتها في علاج الحركات النمطيّة
                    الأوّليّة، ونادرًا ما تُوصَف بها من قبل الأطباء. وكما هو الحال مع العديد من الحالات التي تصيب
                    الأطفال، يؤدّي التحديد والعلاج المبكّران إلى نتائج أفضل.</p>
                <h3>كم عدد الأشخاص المصابين باضطراب الحركات النمطيّة؟</h3>
                <p>الحركات النمطيّة البسيطة كالتأرجح شائعة عند الأطفال الصغار. أمّا الحركات النمطيّة المعقّدة فهي أقلّ
                    شيوعًا بكثير، وتحدث لدى حوالي (3%) إلى (4%) من الأطفال الصغار. يزداد انتشار الحركات النمطيّة بين
                    الأطفال ذوي الإعاقات الذهنيّة، متراوحًا بين (4%) و(16%).</p>
                <h3>ما هي المدّة التي تستغرقها النوبات؟</h3>
                <p>تستمرّ النوبة من ثوان معدودة إلى عدّة دقائق أو أكثر. أمّا تواتُر النوبات فيتفاوت من عدّة مرّات في
                    اليوم الواحد إلى مرّة واحدة كلّ بضعة أسابيع. تتغيّر السلوكيّات بتغيّر السياق، وتحدث عندما ينغمس
                    الشخص في نشاطات أخرى، أو عندما يكون متحمّسًا، أو متوتّرًا، أو متعبًا، أو يشعر بالضجر.</p>
                <h3>ما هي النتائج المحتملة لاضطراب الحركات النمطيّة؟</h3>
                <p>يُظهِر معظم الرضّع التلويح بالذراعين دون هدف ظاهريًّا، وسِمات أخرى لاضطراب الحركات النمطيّة؛ لكن
                    سرعان ما يتخلّصون منها عندما يصلون إلى مرحلة الطفولة. بالنسبة للأطفال الذين يعانون من حركات نمطيّة،
                    يمكن للعلاجات التخفيف من الأعراض في الحالات الخفيفة والمتوسّطة. لكن من المحتمل ألّا تخفّ الأعراض
                    بمرور الوقت لدى الأشخاص ذوي الإعاقات المتعلّقة بالنموّ أو اضطراب طيف التوحّد.</p>
                <h3 class="title">الفرق بين اضطراب التواصل والتوحد</h3>
                <p>إنَّ الفرق الجوهري ما بين اضطراب التواصل والتوحد هو أنَّ التوحد يتمثل بإظهار سلوكيات تخريبيّة أو
                    تكرار القيام بسلوك معين، وفيما يأتي بعضًا من هذه السلوكيات:</p>
                <ul>
                    <li>الإفراط في تكرار بعض من حركات الجسم كرفرفة اليدين أو الدوران أو القفز أو الاهتزاز.</li>
                    <li>شدة تكرار بعض الكلمات والأصوات أو بعض الجمل.</li>
                    <li>الهوس بمواضيع أو أشياء معينة والانشغال فيها.</li>
                    <li>تكرار ترتيب شيء مُعين لعدة مرات والانشغال في ترتيب شيء معين أو تركيبه بمكان معين بالضبط.</li>
                    <li>هوس الانضباط بروتين معين وطقوس معينة في الحياة اليومية.</li>
                </ul>
                <h3>بينما بالنسبة للمصابين باضطراب التواصل فقد تظهر الفروقات لكونهم يظهرون الحالات التالية بعيدًا عن
                    التخريب:</h3>
                <ul>
                    <li>التأخيرات في مهارات الكلام أو اللغة مما يُضفي عليهم طابع عدم الاهتمام بالكلام.</li>
                    <li>رفض استخدام الإيماءات في التعامل كالإشارة أو التلويح.</li>
                    <li>تغيير الموضوع نتيجة عدم القدرة على المناقشة.</li>
                    <li>مقاطعة الآخرين أثناء الكلام بشكل مُستمر.</li>
                    <li>قلة التعبير عن المشاعر والعواطف نتيجة ضعف التعبير.</li>
                    <li>صعوبة في اختيار بعض الكلمات المناسبة أثناء الحديث.</li>
                    <li>صعوبة في تكوين صداقات والحفاظ عليها.</li>
                </ul>
