                    توقفات مفاجئة وحادة في النطق، أو تطويل في نطق بعض الكلمات، بحيث تأتى نهاية الكلمة متأخرة عن بدايتها
                    ومنفصلة عنها أو يظهر العيب في تكرار لأصوات ومقاطع وأجزاء من الكلمة، وعادة تصاحب بحالة من المعاناة
                    والمجاهدة، أو يلوذ الفرد بسلوك تجنبي وهروبي وترتبط التلعثم (التهتهة) بالطفولة وظروف التنشئة
                    الاجتماعية، وتحدث في البنين ثلاثة أمثال حدوثها في البنات، وترتبط بحدوث ظاهرة الشول فالفرد الأعسر
                    أكثر قابلية لها، كما ترتبط بأسباب انفعالية وأسرية.
                </p>
                <p>ويرى بعض الباحثين أن من العلامات الواضحة بالنسبة للتهتهة ألا توجد في كل الأوقات بدرجة واحدة، فقد لا
                    يلجلج الطفل وهو مع قريب له أو عندما يكون بمفرده، ولكنه يلجلج بشدة إذا كان مع آخرين يمثلون السلطة
