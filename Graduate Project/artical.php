                <ul>
                    <p>
                        <li>الاستخدام المفرط للصوت، مثل الصراخ أو التحدث بصوت مرتفع أو البكاء فترة طويلة.</li>
                        <li>التهاب الحنجرة ما يسبب تورمًا في الحبال الصوتية نتيجة الإفراط في استخدام الصوت أو بسبب مرض
                            فيروسي</li>
                        <li>الحساسية فتسبب تورمًا في الحبال الصوتية من السعال وقطرات الأنف والعطس.</li>
                        <li>الارتجاع الحنجري البلعومي، وهو ارتجاع حمض المعدة يسبب تورم وتهيج في الأنسجة الرقيقة للحبال
                            الصوتية والحلق.</li>
                        <li> عقيدات الحبل الصوتي، وهي مسامير صغيرة في الحبال الصوتية تحدث بسبب الإفراط في استخدام الصوت
                            أو إصابة الحبل الصوتي التي تحدث مع الصراخ.</li>
                        <li>الأورام الحميدة في الحبل الصوتي، وهو نمو صغير في الحبل الصوتي يشبه البثرة الناتجة عن الإفراط
                            في استخدام الصوت أو إصابة الحبل الصوتي خلال الصراخ.</li>
                        <li>كيس الحبل الصوتي، نمو صغير في الحبل الصوتي يملأ عادة بالمخاط ويسبب صوتًا خشنًا.</li>
                        <li> الورم الحليمي للحبل الصوتي بسبب عدوى فيروسية في الطيات الصوتية التي يسببها فيروس الورم
