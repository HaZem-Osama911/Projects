                <h3 class="title">اضطراب طيف التوحد (ASD)</h3>
                <p>
                    التوحد، أو اضطراب طيف التوحد (ASD)، هو أحد اضطرابات النمو التي تُعرف مجتمعةً باسم اضطرابات طيف
                    التوحد. يظهر عادةً خلال مرحلة الطفولة المبكرة، وغالبًا قبل سن الثالثة.
                    <br><br>
                    في حين أن شدة وأعراض التوحد تختلف اختلافًا كبيرًا من حالة إلى أخرى، فإن جميع اضطرابات طيف التوحد
                    تؤثر على قدرة الطفل على التواصل والتفاعل مع الآخرين وتكوين علاقات متبادلة.
                </p>
                <h3>التشخيص</h3>
                <p>
                    تشير التقديرات إلى أن 6 من كل 1000 طفل في الولايات المتحدة يُشخَّصون بالتوحد، مع تزايد مطرد في عدد
                    الحالات المُشخَّصة.<br><br> ولا يزال من غير الواضح ما إذا كان هذا الارتفاع يُعزى إلى تحسُّن أساليب
                    التشخيص
                    والتوعية، أم إلى زيادة فعلية في معدل الانتشار، أم إلى مزيج من كلا العاملين.
                </p>
                <p>
                    على الرغم من عدم وجود علاج لمرض التوحد حاليًا، إلا أن العلاجات المكثفة والتشخيص المبكر يمكن أن يحسنا
                    بشكل كبير نوعية حياة الأطفال المصابين بهذا الاضطراب.
                </p>
                <h3 class="title">التحديات التنموية الأساسية</h3>
                <p><span>غالبًا ما يواجه الأطفال المصابون بالتوحد تحديات في ثلاثة مجالات نمو أساسية:</span></p>
                <ul>
                    <li>التفاعل الاجتماعي:</li>
                    <p>صعوبة في إقامة علاقات اجتماعية متبادلة.</p>
                    <li>اللغه</li>
                    <p>تأخر أو ضعف القدرة على التواصل.</p>
                    <li>السلوك</li>
                    <p>السلوكيات المتكررة والاهتمامات المقيدة.</p>
                </ul>
                <p>
                    مع تقدم الأطفال المصابين بالتوحد في السن، قد يطور بعضهم مهارات اجتماعية أفضل ويُظهرون صعوبات سلوكية
                    أقل، حتى أنهم يعيشون حياة طبيعية نسبيًا. ومع ذلك، قد يستمر آخرون في مواجهة صعوبات في المهارات
                    اللغوية والتفاعل الاجتماعي، وقد تتفاقم تحدياتهم السلوكية بمرور الوقت,.<br>

                </p>
                <p>
                    بعض الأطفال المصابين بالتوحد بطيئون في اكتساب المعلومات والمهارات الجديدة، بينما يتمتع آخرون بذكاء
                    متوسط ​​أو أعلى من المتوسط. قد يتعلم هؤلاء الأفراد بسرعة، لكنهم يواجهون تحديات في تطبيق معارفهم في
                    الحياة اليومية أو التكيف مع المواقف المتنوعة.
                </p>
                <p>
                    تُظهر مجموعة صغيرة من الأفراد المصابين بالتوحد مهارات استثنائية في مجالات محددة مثل الفن أو
                    الرياضيات أو الموسيقى - ويشار إليهم باسم "العلماء التوحديين".
                </p>
                <h3>أعراض التوحد</h3>
                <p>
                    تختلف علامات وأعراض التوحد بشكل كبير، حيث يُظهر كل فرد سلوكيات ومهارات فريدة. ومع ذلك، غالبًا ما
                    تتميز الحالات الشديدة بنقص التواصل وعدم القدرة على تكوين روابط اجتماعية.
                </p>
                <h3 class="title">الأعراض الشائعة:</h3>
                <h3>عيوب التفاعل الاجتماعي:</h3>
                <ul class="dot">
                    <li> تأخر تطور الكلام.</li>
                    <li>يتجنب الاتصال المباشر بالعين.</li>
                    <li>يبدو أنه لا يسمع عندما يتم التحدث إليه.</li>
                    <li>يقاوم المودة الجسدية أو ينسحب.</li>
                    <li>يفتقر إلى الوعي بمشاعر الآخرين.</li>
                    <li>يفضل اللعب منفردًا، والانسحاب إلى عالمه الشخصي.</li>
                </ul>
                <h3>التحديات اللغوية:</h3>
                <ul class="dot">
                    <li>تأخر تطور الكلام.</li>
                    <li>فقدان الكلمات أو العبارات المكتسبة سابقًا.</li>
                    <li>يستخدم التواصل البصري فقط للتعبير عن الاحتياجات.</li>
                    <li>يتحدث بنبرة صوت غير عادية أو إيقاع أو لهجة آلية.</li>
                    <li>يواجه صعوبة في بدء المحادثات أو الحفاظ عليها.</li>
                    <li>يكرر الكلمات أو العبارات دون فهم استخدامها.</li>
                </ul>
                <h3>الأنماط السلوكية:</h3>
                <ul class="dot">
                    <li>ينخرط في حركات متكررة مثل التأرجح أو الدوران أو رفرفة اليدين.</li>
                    <li>يطور روتينات وطقوسًا محددة، ويقاوم أي تغييرات.</li>
                    <li>يظهر فرط النشاط أو عدم القدرة على البقاء ساكنًا</li>
                    <li>يركز على تفاصيل أشياء معينة، مثل العجلات الدوارة في سيارة لعبة.</li>
                    <li>يظهر حساسية مفرطة تجاه المدخلات الحسية، مثل الضوء أو الصوت، بينما يبدو غير مبالٍ بالألم.
                    </li>
                </ul>
                <p>يواجه بعض الأطفال الصغار صعوبة في مشاركة تجاربهم مع الآخرين أو التكيف مع التغيرات في بيئتهم.</p>
                <h3>أسباب وعوامل خطر التوحد</h3>
                <p>لا يزال السبب الدقيق لمرض التوحد غير واضح، على الرغم من أن الأبحاث تشير إلى أن مجموعة من العوامل
                    الوراثية والبيئية تلعب دورًا مهمًا.</p>
                <h3>العوامل الوراثية:</h3>
                <p>حدد الباحثون عدة جينات يُحتمل ارتباطها بالتوحد. بعض هذه الجينات يزيد من احتمالية الإصابة بهذا
                    الاضطراب، بينما يؤثر بعضها الآخر على نمو الدماغ والترابط بين الخلايا العصبية. قد تكون الطفرات
                    الجينية موروثة أو تحدث تلقائيًا.</p>
                <h3>العوامل البيئية:</h3>
                <p>تنشأ العديد من الحالات الصحية نتيجة تفاعل العوامل الوراثية والبيئية. ويستكشف الباحثون ما إذا كانت
                    العدوى الفيروسية أو السموم البيئية قد تُسهم في الإصابة بالتوحد.
                <h3>نظريات إضافية:</h3>
                <p>تشمل العوامل الأخرى المحتملة قيد البحث مضاعفات الولادة ودور الجهاز المناعي في التوحد. ويشير بعض
                    الباحثين أيضًا إلى أن تلف اللوزة الدماغية، وهي منطقة في الدماغ مرتبطة بالتنظيم العاطفي، قد يكون
                    عاملًا مؤثرًا.</p>
                <h3 class="title">عوامل الخطر المعروفة:</h3>
                <h3>الجنس:</h3>
