                <p>أما إذا كانت المشكلة وظيفية فهنا يتم الاعتماد على الجراحة، فإذا كانت مشكلة في سقف الحلق يتم تركيب
                    بعض الغضاريف لتي تساعد على إطالته، أما إذا كانت في اللحمية الخلفية يتم استئصالها، وفي حال وجود
                    ضيق في فتحة الأنف فيتم إجراء عملية جراحية لتوسيعها.</p>
                <h3 class="title">السرعة الزائده في الكلام</h3>
                <h3>اضطراب السرعة الزائدة في الكلام :</h3>
                <br>
                <p>يعني الاضطراب السريع في الحديث هو وجود خلل في النطق بالكلام، ويكون له صلة ببعض المشاكل التي تحدث
                    أثناء خروج الكلام، وهذا لأنه يقوم بالحديث بسرعة مطلقة، ويكون هذا الاضطراب معرض لوجود تأتأة في
                    الكلمات، أو عدم وجود التوازن في الحديث، وفقدان بعد الحروف.</p>
                <br>
                <p>ولا يوجد أي إثبات على وجود دلائل علمية وراء هذه التأتأة، وبعد الدراسات التي أقيمت تم التأكد من أن
                    السبب الرئيسي في هذا الاضطراب هو عدم تنظيم الأفكار واستخراج الكلام، حيث هناك بعض الخصائص التي
                    تنتج عن اضطراب السرعة الزائدة في الكلام، سوف نتعرف عليها من خلال المقال ونتعرف على طرق علاج
                    اضطراب السرعة الزائدة في الكلام</p>
                <h3>علامات اضطراب السرعة الزائدة في الكلام :</h3>
                <p>1-يجب الانتباه لوجود اشتباه في طريقة نطق الكلام، فهذا يدل على أن الصوت يسير على وتر واحد من
                    الأحبال الصوتية.
                </p>
                <p>2-عدم المقدرة على خروج معظم الكلمات بصورة صحيحة، وهذا يكون بسب استخراج الكلمات السريعة، ولكن مع
                    التكلم بطريقة عادية يستطيع الشخص أن يوضع ويفسر من ما يقوله من كلام ويكون له نغمة أخرى غير التي
                    يتحدث بها مسرعًا.</p>
                <p>3-الكلام المتشابك ووقوع بعض الكلمات بنطق غير سليم، وهذا وراء التحدث مسرعًا، ويكون بالأخص الأحاديث
                    السريعة والطويلة، في مثل كلمة (مستشفى) تنطق (مسشفى)، والعديد غيرها من الكلمات التي يمكن بسهوله
                    وقوع الأحرف فيها، ولكن عند لتحدث ببطيء يقوم بنطق جميع الأحرف.</p>
                <p>4-الجمل الطويلة والتي تتغير بها مواقع الأوتار الصوتية مثل: (جملة كيف حالك)، تنطق (حيف كالك) فهذه
                    تكون من الأمور المحرجة للكثير. </p>
                <p>5-ظاهرة التأتأة في الكلمات هي تكون نتيجة لخلل في بعض الأوتار الصوتية، وعدم توازن لغوي.</p>
                <p>6-لا يكون مفهوم اضطراب الحديث مقصود به الأشخاص الذين يتحدثون بسرعة ولكن جميع أحاديثهم صحيحة ويوجد
                    بها لباقة، لأن هذا له أشخاص متدربون على هذا الحديث.</p>
                <h3>أسباب اضطراب السرعة الزائدة في الكلام :</h3>
                <p>بعد البحث توصل العلماء أنه لا يوجد أي أسباب مقنعة تهدف إلى وجود اضطراب السرعة في الكلام، ولكن
                    هناك سبب واحد تم توقعه وهو العوامل الوراثية وهذا تم الإعلان به في عام (1964)، ولكن هذا لا يعتمد
                    به في الكثير من الحالات.</p>
                <br>
                <p>وهناك بعض العلماء التي أخبرت أن تلك الاضطراب وراء عدة أسباب مختلفة في المخ، وهي التي تكون مسئولة
                    عن الحركة والتخاطب.</p>
                <br>
                <h3>علاج اضطراب السرعة الزائدة في الكلام :
                </h3>
                <p>التشخيص الجيد لحالات اضطراب السرعة الزائدة في الكلام من الأمور الضروري التي يجب أن تتوفر في
                    الطبيب المعالج، ومن خلال حالة التشخيص يتم علاج الحالات المصابة باضطراب السرعة الزائدة في الكلام،
                    وهذا يكون من الشئون الخاصة بالأطباء كافة، والعلاج يكون على تحديد نوع الاضطراب.

                </p>
                <br>
                <p>حيث يقوم الطبيب بمعالجتهم نفسيًا في البداية، وبعد ذلك يقوم بالاهتمام بتقويم الاستراتيجيات التي
                    تساعدهم على التخلص من هذا السلوك.

                </p>
                <h3>طرق التخلص من السرعة الزائدة في الكلام :</h3>
                <ul>
                    <li> العلاج باللباقة :</li>
                    <p>حيث يكون هذا عن طريق الاهتمام بطرق الحديث، وما مدى صدوره بطلاقة ووضوح -وبطء، وهذا من خلال
                        التطبيق على بعض الكلمات البسيطة، وهذا من خلال الضغط على الحروف الساكنة والمتحركة في الكلام،
                        ومن خلال المراقبة المستمرة من الطبيب يمكنه تحديد مدى شفاء المصاب في وقت ما.
                    </p>
                    <li>العلاج بالسيطرة على النفس :</li>
                    <p>يجب أن يكون الشخص متخذ طريقة العلاج على محمل الجد، ويريد أن يتم شفاءه من تلقاء نفسه، وهذا من
                        خلال معرفة الكلمات التي يتم النطق بها وكيفية موضعها، وعدم وقوع أي حرف من الجمل، والتخاطب
                        بهدوء شديد وعدم وجود أي تلعثم في الجمل أو الكلمات، فقط هذا من أكبر العوامل التي تساعد على
                        التخلص من اضطراب السرعة في الكلام.

                    </p>
                    <li> العلاج بالتغيير :</li>
                    <p>يجب أن يكون الشخص المصاب مؤمن بفكرة أنه يقوم بتحمل تلك العوامل حتى يتخلص من التلعثم في
                        الكلام، ويعيش حياة طبيعية، ويقوم بالابتعاد عن التوتر و الاكتئاب والقلق المتزايد، وهذا من
                        أكثر طرق النجاح في الشفاء.
                        <li> التحدث الكثير من مصابي اضطراب السرعة في الكلام :</li>
                    <p>من العوامل الأساسية التي تساعد على التخلص من هذه المشكلة هو التدريب المتزايد على الحديث مع
                        الأشخاص الطبيعيين، والتكلم معهم بالباقة المتكاملة والحديث المفسر، وتعريضهم لقول الجمل التي
                        يوجد بها بعض اللخبطة لنعرف ما مدى الوقت الذي يستطيعون التكلم فيه بسلاسة ووضوح.</p>
                    </p>
