                        <li>شلل الحبل الصوتي، قد يولد الطفل بتشوّه في الحنجرة أو مشكلة عصبية تسبب بحة في الصوت. يمكن أن
                            تشمل أسباب بحة الصوت هذه شلل الحبل الصوتي، وتشنج الحنجرة، والخراجات، والشقوق في صندوق الصوت.
