                <h3 class="title">اللثغة</h3>
                <br>
                <h3>ما هي اللثغة؟</h3>
                <p>هى إبدال الصوت بصوت آخر نتيجة لخروج الصوت من مخرج غير مخرجه الصحيح، أكثرها شيوعًا بين الأطفال هي
                    السين وتزول كلما كبر الطفل، تليها الراء وقد تستمر مع الطفل أو تزول، وبعدها في بعض الحروف الحلقية
                    الأخرى مثل الكاف وغيرها.</p>
                <p>أولاً: اللثغة السينية، أو الثأثأة، وتعد أكثر عيوب النطق انتشاراً بين الأطفال حتى سن السابعة، وقد تزول
                    أو تستمر وتحتاج لعلاج تخاطبي.</p>
                <p> يتطلب نطق صوت (س) أن يكون اللسان خلف حافة الأسنان العليا ولا يلامس طرف اللسان الأسنان بينما ما يحدث
                    أن
                    الصغير يبدل الصوت بالثاء في الغالب، وأحيانًا بالشين في فتكون سكر هي ثكر أو شكر وذلك لعدة أسباب. إما
                    لعدم
                    قدرة الطفل على التحكم فى حركات لسانه أو إلى بروز طرف اللسان خارج الفم أو لسبب آخر من الأسباب التى
                    ترجع
                    إلى الناحية التشريحية فى تكوين اللسان.</p>
                <p>ثانياً: اللثغة الرائية وهي النسبة الأقل وفى هذا النوع من اللثغة ينطق الطفل صوت الراء إما ياء أو غين
                    أو لام، فتنطق كلمة (سراب) (سياب أو سغاب أو سلاب).</p>
                <p>ثالثاً: اللثغة فى بعض الأصوات الحلقية مثل الكاف والجيم والقاف والغين والخاء، وهي الأقل انتشارًا وإن
                    ظهرت بين الأطفال تزول أسرع فينطق الطفل كلمة (كتاب) (تتاب) وكلمة مثل جردل ينطقها دردل، وهكذا.</p>
                <br>
                <h3>ما أسباب حدوث اللثغة؟</h3>
                <p>- هناك عدة أسباب منها العضوية وتتمثل فى وجود عيب خلقي في اللسان أو الحلق أو الأسنان بما يتعذر معه
                    إخراج بعض الأصوات من مخرجها الطبيعي.
                </p>
                <p>- ومن تلك العيوب الخلقية الشفة الأرنبية في العليا أو السفلى، أو عدم انتظام الأسنان أو زيادة حجم الفك
                    العلوي أو السفلي أو مشكلة في اللسان مثل حجمه أو بروزه أو غير ذلك.</p>
                <p>- ومن الأسباب أيضًا أسباب مكتسبة مثل تقليد المحيطين بالطفل أو اكتساب لثغة بسبب لغة أخرى غير مثل
                    الفرنسية وغير ذلك.</p>
                <br>
                <h3>التشخيص والعلاج</h3>
                <p>نقابل الطفل ويقوم الطبيب بإجراء حوار معه لمعرفة أي الحروف التي يعاني فيها من لثغة ثم يجري فحص الطفل
                    إكلينيكيًا للتأكد من السبب.</p>
                <p>بعد التشخيص يتحدد أسلوب العلاج المناسب فإذا كانت الأسباب عضوية يصبح العلاج الجراحي هو الحل أما إن
                    كانت الأسباب المكتسبة فإن العلاج يتمثل في التدريبات اللغوية.
                </p>
                <br>
                <h3>
                    التدريبات اللغوية
