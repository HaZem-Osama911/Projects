                <h3>التعامل مع اضطراب التواصل والتوحد</h3>
                <p>قد تختلف طرق التعامل مع مصابي اضطراب التواصل والتوحد، فيما يأتي مجموعة من النصائح للتعامل مع كلاً من
                    اضطراب التواصل والتوحد</p>
                <h3>التعامل مع اضطراب التواصل</h3>
                <p>فيما يلي مجموعة من النصائح التي يمكن اتباعها عند التعامل مع ذوي اضطراب التواصل:
                    <br>
                    -مشاركة الطفل في قراءة كتاب وفتح باب النقاش المفتوح حول الكتاب لتشجيع الطفل على الحديث، كما يمكن
                    المناقشة حول الشخصيات الموجودة في الكتب وتفسير سلوكياتهم.
                    <br>
                    -التحدث عن الموضوعات الحياتية التي قد تواجه الطفل فيمكن التفكير سويةً مع الطفل في مشاعر الآخرين
                    سواءً أكانوا أصدقاءً أو زملاء دراسة عند تعرضهم لمواقفٍ مُعينة.<br>
                    -إفساح المجال لتوقعات الطفل من خلال التوقف عند نقطة معينة عند قراءة كتاب معين ومناقشة الطفل في
                    توقعاته حول ما سيحدث بعد ذلك ويمكن البحث معًا عن أدلة في القصة قد تُساعد على إيجاد التوقع
                    الصحيح.<br>
                    -الاستعانة ببعض الأدوات المرئية التي تساعد الطفل على استكمال المحادثات أو بدءها.<br>
                    -الالتزام بممارسة الأنشطة الاجتماعية بشكلٍ منتظم وتحديد وقت معين لبدء النشاط وانتهائه بحيث يُشارك
                    الطفل.

