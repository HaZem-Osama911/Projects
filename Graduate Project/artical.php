                <h3>وأما طرق العلاج النفسي فهي متعددة نذكر منها:</h3>
                <p>-طريقة اللعب: حيث أدت هذه الطريقة إلى كشف اضطرابات الأطفال وتفهم أسبابها وخاصة صغار السن، كما أن
                    اللعب هدف تشخيصي إذ يمكن من خلاله الوصول إلى المعلومات والرغبات المكبوتة والإضطرابات المتفرعة.</p>
