                    <h3 class="title">أعراض تخصّ الكلام</h3>
                    <p>يشير النطق أو الكلام إلى إنتاج الأصوات التي تسمح للأفراد بالتعبير عن أنفسهم. يُنتَج الكلام بدقّة
                        عن
                        طريق تنسيق حركات العضلات في الرأس، والرقبة، والصدر، والبطن. تطوير النطق هو عملية تدريجيّة تحتاج
                        لسنين من الممارسة. ويتعلّم المرء أثناء تطوير النطق كيفيّة تنظيم هذه العضلات؛ ليُنتِج محادثة
                        مفهومة.
                        <br>
                        <br>
                        إنّ عدم الطلاقة فئة من فئات اضطرابات الكلام أو النطق. يتّصف اضطراب الطلاقة الذي يبدأ في مرحلة
                        الطفولة -والمشار إليه سابقًا بالتلعثم- بتعطّل تدفّق الكلام، ويتضمّن تكرار أصوات الكلام، و/أو
                        التردّد
                        قبل الكلام وأثنائه، و/أو مدّ أصوات الكلام. يعاني الأشخاص ذوي اضطرابات النطق عمومًا من صعوبات في
                        التلفّظ، أي مشاكل في تكوين الأصوات وجمعها، تُترجَم عادةً بحذفها، أو تحريفها، أو استبدالها.
                        <br>
                        <br>
                        تشمل اضطرابات الصوت صعوبات في جودة الصوت، وطبقته، وعلوّه. قد يواجه الأشخاص ذوي اضطرابات الصوت
                        مشكلة
                        في الشكل الذي يبدو عليه صوتهم، والمستمعون في فهمهم.
                    </p>
                    <h3 class="title">أعراض تخصّ المعالجة السمعيّة</h3>
                    <p>المعالجة السمعيّة مصطلح يصف ما يحدث في دماغ الشخص عندما يتعرّف على الأصوات المحيطة به ويفسّرها.
                        تنتقل
                        الموجات الصوتيّة التي نسمعها عبر الأذن إلى العصب السمعيّ، وتتغيّر بعد ذلك إلى نبضات كهربائيّة
                        على
                        الدماغ تفسيرها.
                        <br>
                        <br>
                        وفقًا للتصنيف الإحصائيّ الدوليّ للأمراض والمشاكل المتعلّقة بالصحّة (بالإنجليزيّة: International
                        Statistical Classification of Diseases and Related Health Problems، واختصاره: ICD)، يتّصف اضطراب
                        المعالجة السمعيّة (بالإنجليزيّة: Auditory Processing Disorder، واختصاره: APD) بخلل في المعالجة
                        السمعيّة؛ ممّا يؤدّي إلى قصور في التعرّف على الأصوات وتفسيرها من قبل الدماغ. يُعرَف اضطراب
                        المعالجة
                        السمعيّة أيضًا باضطراب المعالجة السمعيّة المركزيّة (بالإنجليزيّة: Central Auditory Processing
                        Disorder، واختصاره: CAPD). لا يدخل اضطراب المعالجة السمعيّة المركزيّة ضمن الدليل التشخيصيّ
                        والإحصائيّ الخامس للاضطرابات النفسيّة.
                        <br>
                        <br>
                        لا يدرك الطفل ذو اضطراب المعالجة السمعيّة عادةً الفروق الدقيقة بين الحروف، حتّى إذا كانت الأصوات
                        عالية وواضحة. على سبيل المثال، طلبت من طفلك ما يلي: «أخبرني ما التشابه بين السيّارة والطيّارة».
                        قد
                        يسمع الطفل ذو اضطراب المعالجة السمعيّة هذا الطلب كما يلي: «أخبرني ما التشابه بين الصنّارة
                        والطيّارة»، أو «أخبرني ما التشابه بين الصنّارة والنظّارة».
                    </p>
                    <h3 class="title">أسباب اضطرابات التواصل</h3>
                    <p>تتضمّن أسبابُ مشاكل التواصل فقدانَ السمع، والاضطرابات العصبيّة، وإصابة الدماغ، وإصابة الأحبال
                        الصوتيّة، والتوحّد، والإعاقة الذهنيّة، وإساءة تعاطي الموادّ، والإعاقات الجسديّة مثل الشفّة
                        المشقوقة
                        والشقّ الحلقيّ، والاضطرابات العاطفيّة أو النفسيّة، واضطرابات النموّ.<br>
                        يفصل الدليل التشخيصيّ
                        والاحصائيّ الخامس للاضطرابات النفسيّ بشكل عامّ بين الحالات الطبّيّة والعصبيّة المتمايزة وبين
                        اضطرابات التواصل. في كثير من الأحيان يكون السبب وراء اضطراب التواصل مجهولًا.</p>
                    <h3 class="title">علاج اضطرابات التواصل</h3>
                    <p>إنّ الطريقة المثلى لمقاربة علاج اضطرابات التواصل هي التركيز على الوقاية والتدخّل المبكّر.</p>
                    <p>إذا كان لديك أيّ قلق متعلّق بتطوّر النطق أو اللغة لدى طفلك، عليك التواصل مع طبيب العائلة الذي قد
                        يحيلك إلى اختصاصيّ علاج النطق واللغة. اختصاصيّ علاج النطق واللغة هو اختصاصيّ متمرّس في تقييم
                        الأشخاص
                        ذوي اضطرابات النطق، أو اللغة، أو الصوت، أو السمع، أو البلع، والتي تعيق قدرتهم على التواصل، وفي
                        التعامل معهم.
                        <br>
                        <br>
                        بناءً على نتائج الاختبارات، قد يوصيك اختصاصيّ علاج النطق واللغة بممارسة نشاطات تفاعليّة قائمة
                        على
                        التواصل بينك وطفلك. يمكن لهذه النشاطات أن تتضمّن القراءة لطفلك بانتظام، أو نطق كلمات وجمل بسيطة
                        فيقلّدها الطفل بنجاح، أو إعادة ما ينطقه الطفل بشكل صحيح. على سبيل المثال، إذا قال الطفل: «الكرة
                        أوله»، يمكنك الردّ بما يلي: «أجل، الكرة أسفل الطاولة». يسمح لك ذلك بضبط نطق طفلك ولغته دون
                        التصحيح
                        له؛ إذ إنّ التصحيح المستمرّ للطفل قد يجعل الكلام عمليّة غير سارّة بالنسبة له. قد يقترح اختصاصيّ
                        علاج
                        النطق واللغة أيضًا العلاج الفرديّ أو الجماعيّ.
                    </p>
                    <h3 class="title">المعالم النمائيّة منذ الولادة إلى عمر (5) سنوات</h3>
                    <p>على الوالدَين أن يكونا على دراية بالأعمار النموذجيّة التي تَفرض على الطفل -عند بلوغ كلّ منها-
                        تحقيق
                        معلم من معالم النموّ.
                        <br>
                        <br>
                        تشكّل أوّل (3) سنوات من الحياة الفترة الأكثر أهمّيّة في التحصّل على مهارات النطق واللغة، ويكون
                        فيها
                        الدماغ في تطوّر ونضوج. تتطوّر مهارات النطق واللغة بشكل أفضل في عالم غنيّ بالأصوات، والمشاهد،
                        والتعرّض المستمرّ لكلام الآخرين، ولغتهم. <br>عندما يبلغ الطفل من (4) إلى (6) أشهر، يبدأ الطفل
                        عادةً
                        بالمناغاة بطريقة تشبه الكلام، مستخدمًا أصواتًا متنوّعة، والاستجابة للتغيّرات في نبرة الصوت.<br>
                        بعد
                        عام
                        واحد، من الطبيعيّ أن يفهم الطفل مثلًا بعض الكلمات البسطة التي تدلّ على الأشياء، وأن يتواصل عبر
                        الإيماءات الجسديّة، وأن يستخدم كلمة أو كلمتين. <br>بعد مضيّ (18) شهرًا، يستطيع أغلب الأطفال نطق
