                <h3 class="title">الحبسة الكلامية | Aphasia</h3>
                <p>الحبسة الكلامية هي عدم القدرة على النطق والإعراب عن النفس والتعبير عن الفهم سواء بالكلام، أو الكتابة،
                    أو الإشارة، وصعوبة الكلام المفاجئ.</p>
                <br>
                <p>هناك مركزان على جانبي الدماغ، ويكون الجانب الأيسر هو المسيطر مع الأشخاص الذين يعتمدون على يدهم
                    اليمنى، وتشمل المنطقة النهاية الخلفية للتلفيف الأمامي والتي تشمل مركز بروكا الكلامي (تسمى حبسة
                    بروكا) والنهاية الخلفية للتلفيف الصدغي الأول. لذا تكون الحبسة في العادة مصحوبة بنوع من أنواع الشلل
                    النصفي الذي يصيب الجانب الأيمن من الجسم.</p>
                <br>
                <p>يكمن الفرق بين الحبسة الكلامية والتلعثم في أن الأولى تتمثل في صعوبة فهم الكلام والتحدث أو النطق بشكل
                    كلي أو جزئي، أما الثانية فهي مشكلة تتمثل في تكرار بعض أحرف الكلام، أو التداخل الصوتي عند التحدث مما
                    يجعل الكلام غير واضح</p>
                <h3>انواع الحبسة الكلامية</h3>
                <p>يمكن تقسيم الحبسة إلى عدة أنواع، أهمها ما يلي:</p>
                <ul>
                    <p>
                        <li>الحبسة التعبيرية</li> وتسمى أيضاً باسم حبسة بروكا، وهي صعوبة التحدث بجمل طويلة، فيعبر المريض
                        عما يريده بصعوبة من خلال ذكر كلمة واحدة أو جملة قصير، وقد يفهمه المحيطون به غالباً، كما يستطيع
                        المصابون بذلك النوع فهم ما يقوله غيرهم.
                    </p>
                    <p>
                        <li>الحبسة الاستقبالية أو حبسة فيرنيك</li>ويواجه المصابون بها صعوبة في القراءة والكتابة، ويمكنهم
                        التحدث بجمل طويلة غير مفهومة، ويسمى ذلك النوع أيضاً باسم الحبسة الشاملة.
                    </p>
                    <p>
                        <li>الحبسة الذكرية أو العامة</li> التي تتميز بعدم قدرة المريض على إيجاد الكلمات المناسبة أو
                        التحدث.
                    </p>
                    <p>
                        <li>الحبسة التقدمية الأولية</li>وهي فقدان تدريجي للقدرة على القراءة، والكتابة، والتكلم، وفهم ما
                        يقوله الآخرون، وتحتاج تشخيصاً دقيقاً لتفريقها عن أمراض أخرى مثل ألزهايمر، ولا يوجد حتى الآن علاج
                        محدد لتلك الحالة غير المجموعات الداعمة وغيرها من الطرق التي قد تساعد.
                    </p>
                </ul>
                <br>
                <p>كذلك قد يعاني البعض من فقدان النطق النفسي، وهو صعوبة الكلام بسبب التعرض لصدمة نفسية شديدة، وينقسم إلى
                    نوعين رئيسين هما:</p>
                <ul>
                    <li>فقدان النطق الجزئي، حيث يفهم المريض كلام المحيطين به مع صعوبة التعبير عما يريد أو التحدث ببضع
                        كلمات بدلاً من الجمل الكاملة.</li>
                    <li>فقدان النطق الكلى، بحيث لا يمكنه التعبير نهائياً عن نفسه سواء بالكلام أو الإيماء، بالإضافة إلى
                        فقدان القدرة على فهم كلام المحيطين.</li>
                </ul>
                <p>كذلك قد يتحدث البعض بكلمات غير مفهومة، كما قد يفقد القدرة على القراءة والكتابة.</p>
                <h3> علاج الحبسة الكلامية</h3>
                <p>يعتمد علاج الحبسة الكلامية على علاج المسبب؛ على سبيل المثال يعتمد علاج فقدان النطق المفاجئ الناجم عن
                    إصابة الرأس على مرور الوقت، فقد يتعافى المريض مع مرور الوقت ويبدأ التحسن بعد عدة أشهر، ويمكن استعادة
                    مهارة النطق بدون علاج أو بعلاج تعليمي لإعادة تأهيل المهارة اللغوية، وقد يستمر فقدان القدرة على
                    الكلام عند البعض، لذا يمكن اللجوء إلى العلاج بالنطق ومساعدة المريض على النطق والتعافي.</p>
                <br>
                <p>وجد أن قدرات اللغة والتواصل قد تستمر في التحسن لسنوات عديدة، وتشمل العوامل التي تؤثر على مقدار
                    التحسن، منطقة الدماغ المتضرر، ومقدارها، وعمر المريض، وصحته الجسدية.</p>
                <p>تهدف إعادة تأهيل التخاطب إلى تحسين قدرة المريض على التواصل بالكلام من خلال مساعدته على استخدام
                    القدرات اللغوية لديه وتحسينها، وتعلم طرق أخرى للتواصل مثل الإيماءات، أو الصور، أو استخدام الأجهزة
                    الإلكترونية الحديثة.</p>
                <p>كذلك قد يلجأ الطبيب إلى جلسات العلاج المعرفي، ومساعدة المريض على تنمية مهارات تجاوز الأزمات النفسية
                    والتعامل معها في حال كان سبب الإصابة نفسياً، كما قد يلجأ أحياناً إلى التنويم المغناطيسي مع المرضى
                    الرافضين للمساعدة، أو غير المستجيبين للطرق العلاجية الأخرى، وفي بعض الحالات يمكن استخدام مضادات
                    الاكتئاب أو مضادات القلق أيضاً، بالإضافة إلى تمارين الاسترخاء،، والتنفس العميق، واليوجا.</p>
                <h3>ادوية لعلاج الحبسة الكلامية</h3>
