                    والعواطف والإتجاهات.</p>
                <p>-فحص مشكلات المصاب باللجلجة مع والديه: حيث أن اضطرابات المتلجلج تظهر في البيت وتتصل بالطريقة التي
                    يعامله فيها أبواه، وتهتم هذه الطريقة بإرشاد الوالدين إلى أفضل السبل والطرق في التربية ومعاملة
                    الأبناء</p>
                <p>-طريقة الإيحاء والإقناع: وهي تتبع لمعالجة مخاوف الطفل الناجمة عن عيوب الكلام وعن الإحباط الشديد وتهدف
                    إلى بناء شخصية المصاب والإيحاء له بأن حالته في تحسن وأن الإضطراب لن يستمر طويلاً.</p>
                <p>-الإسترخاء: حيث أن اللجلجة عرض جسمي لإضطراب نفسي وهنا يتدرب المريض على إرخاء عضلاته وتخفيف النشاط
                    الذهني والنفسي والإنفعالي، وأن يكون المهم هنا هو الوصول إلى الأسباب الحقيقية لمعالجة الإضطراب وتحقيق
                    الإستقرار النفسي والعقلي إلا أن هذه الطريقة تعتبر عاملاً مساعداً على تحقيق هذا الهدف.</p>
                <br>
                <h3>أما طرق العلاج الكلامي فتتمثل بالآتي:</h3>
                <p>-طريقة الإسترخاء الكلامي: وتهدف هذه الطريقة إلى التخلص من الإضطرابات في اللجلجة أثناء الكلام وتكوين
                    الشعور باليسر وفيها تمرينات على الأصوات المتحركة والساكنة ثم الكلمات والجمل وذلك من خلال نطقها في
                    رخاوة وأناة وفي نغم بطيء ويمكن لهذه الطريقة من إحداث تحسن وقتي ويخشى من النكسة المفاجئة بعدها.</p>
                <p>-تمارين الكلام الإيقاعي: وهنا تتبع بعض الوسائل الإيقاعية مثل النقر أو الصفير أو الإشتراك بالقراءة
                    الجماعية الجهرية.</p>
                <p>-طريقة النطق بالمضغ: حيث يتصور المصاب أنه يمضغ قطعة من اللحم أو اللبان وأن يكون لمضغه صوت وهذا ما
                    يحول انتباهه عن لجلجته ويخفف من حدة مخاوفه.</p>
                <p>-فك إشراط القلق: حيث يحدث هذا باستخدام جهاز المتروتوم الذي يشبه سماعة الأذن والذي يمكن ضبط دقاته وعلى
                    المصاب أن ينطق كلمة واحدة مع كل دقة وفي البداية نضبطه على عدد قليل من الدقات في الدقيقة ثم نبدأ
                    بزيادة الدقات حتى تصل لمستوى الكلام العادي ومن الكلام أمام الناس بالدخول في مجموعات بشرية تتسع كلما
                    حصل التحسن التدريجي.</p>
                <p>-طريقة العلاج الظلي: ويكون العلاج هنا بالوقوف أمام المرآة وممارسة الكلام المضطرب والذي يدفع المصاب
                    إلى ملاحظة عيوبه والسعي إلى تصحيحها وتشكل الرغبة في التخلص منها.</p>
                <br>
                <p>ولابد في كل الأحوال من تآزر العلاج النفسي مع العلاج الكلامي للوصول إلى أفضل النتائج بغية بث روح
                    الإستقرار والهدوء في نفس المصاب وعلى المعالج أن يكشف عن الحالة ويحدد نوع الإصابة وشدتها وطرق علاجها
                    بتبصر وحكمة دقيقة للوصول إلى الأهداف المنشودة</p>
